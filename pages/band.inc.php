    <h2>Das ist Like A Cat!!!</h2>

    <p>Im Sommer 2012 gegründet und im Frühling 2013 das erst mal Live on Stage.</p>

    <p>Das erste halbe Jahr ihrer Bandgeschichte haben Julia (Gesang), Alex (Gitarre) und Manu (Schlagzeug) in ihrem Proberaum verbracht - mit dem obersten Ziel ohne ein Cover und dafür mit viel mehr Kreativität <em>Like A Cat</em> ins Leben zu rufen!</p>

    <p>Was entsteht, wenn ein Hardcore Schlagzeuger, einen typischen Hardrock Gitarrist und eine Sängerin mit einer klaren warmen Stimme zusammen kommen, ist schwer zu beschreiben aber schön anzuhören...</p>

    <p>Like A Cat schafft es jeden Song anders klingen zu lassen ohne dabei den roten Faden zu verlieren. Live erwartet den Zuschauer eine abwechslungsreiche Show von schnellen Songs mit Breakdown-Parts bishin zu gefühlvollen Balladen. Sie bedienen sich an nahezu allen Stilmitteln der verschiedener Rock-Genren. Diese Mischung macht Like A Cat immer interessant und nie langweilig, da man nie weiß was als nächstes kommt.</p>

